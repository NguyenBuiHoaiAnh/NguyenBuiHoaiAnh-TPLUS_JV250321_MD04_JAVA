public class Person {
    private String name;
    private int age;

    // Setter cho name
    public void setName(String name) {
        this.name = name;
    }

    // Getter cho name
    public String getName() {
        return name;
    }

    // Setter cho age
    public void setAge(int age) {
        this.age = age;
    }

    // Getter cho age
    public int getAge() {
        return age;
    }
}
